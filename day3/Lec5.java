 /*  Increment/decreament Example....... */
 
 class Lec5 {
    public static void main(String[] args) {
        int a=10; 
        System.out.println(a);
        System.out.println("PrePost Incr.. ");
        System.out.println(a++); //10 (11)
        System.out.println(++a);//11  (12)


        System.out.println("PrePost decr.. ");
        System.out.println(a--); //10 (11)
        System.out.println(--a);//11  (12)
    }
}
